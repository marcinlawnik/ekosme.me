                    Słowo wstępne
                </h1>
            </div>
            <div class="">
                <p class="justified">
                    Kolejny rok szkolny, 2014/2015 za nami. Od 5 listopada prowadzę ekosme.me, portal ze śmiesznymi (lub też nie) obrazkami związanymi z EKOSem.
                    Pierwsza wersja powstała w ciągu 5 dni. Stwierdziłem, że warto zrealizować akurat ten pomysł, i się nie zawiodłem.
                </p>
                <p class="justified">
                    W związku z tym, że udało się zebrać pieniądze na przedłużenie domeny na kolejny rok, przez taki właśnie czas co najmniej będę prowadził serwis.
                    W ciągu wakacji pracuję nad nową funkcjonalnością, aby strona lepiej spełniała oczekiwania użytkowników i była bardziej przejrzysta.
                </p>
                <p class="justified">
                    Postaram się, aby w roku szkolnym 2015/2016 memy ukazywały się co 2-3 dni. Może być to trudne, ponieważ w EKOSie chyba aż tyle się nie dzieje, ale będę się starał.
                    W tym celu nawiązałem współpracę z kilkoma uczniami naszej szkoły, którzy będą pomagać w prowadzeniu ekosme.me.
                    Poniżej zamieszczone sa podziękowania, a także wszystkie memy z tego sezonu łącznie z informacją, jaką popularnością się cieszyły.
                    Miłego oglądania!
                </p>
                <br>
                <p class="signature">
                    Marcin Ławniczak
                </p>
